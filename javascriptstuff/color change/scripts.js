function changeRed(){
    document.body.style.backgroundColor = 'red';
}

 function changeOrange(){
    document.body.style.backgroundColor = 'orange';
}

 function changeYellow(){
    document.body.style.backgroundColor = 'yellow';
}

 function changeGreen(){
    document.body.style.backgroundColor = 'green';
}

 function changeBlue(){
    document.body.style.backgroundColor = 'pink';
}

document.getElementById('color-red').addEventListener('click', changeRed)

document.getElementById('color-orange').addEventListener('click', changeOrange)

document.getElementById('color-yellow').addEventListener('click', changeYellow)

document.getElementById('color-green').addEventListener('click', changeGreen)

document.getElementById('color-blue').addEventListener('click', changeBlue)